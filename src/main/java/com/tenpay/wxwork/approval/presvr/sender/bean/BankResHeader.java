package com.tenpay.wxwork.approval.presvr.sender.bean;

import com.fasterxml.jackson.annotation.JsonIgnore;
import com.fasterxml.jackson.annotation.JsonProperty;

public class BankResHeader {
	@JsonProperty("bank_appid")
	String bank_appid;

	@JsonProperty("org_id")
	String org_id;

	@JsonProperty("trans_code")
	String trans_code;

	@JsonProperty("msg_no")
	String msg_no;
	
	@JsonProperty("msg_time")
	String msg_time;
	
	@JsonProperty("sign_type")
	String sign_type;

	@JsonProperty("ver")
	String ver;
	
	@JsonProperty("retcode")
	String retcode;
	
	@JsonProperty("errmsg")
	String errmsg;
	
	@JsonIgnore
	public String getBankAppid() {
		return bank_appid;
	}

	@JsonIgnore
	public void setBankAppid(String bank_appid) {
		this.bank_appid = bank_appid;
	}


	public String getOrgId() {
		return org_id;
	}

	@JsonIgnore
	public void setOrgId(String org_id) {
		this.org_id = org_id;
	}

	@JsonIgnore
	public String getTransCode() {
		return trans_code;
	}

	@JsonIgnore
	public void setTransCode(String trans_code) {
		this.trans_code = trans_code;
	}

	@JsonIgnore
	public String getMsgNo() {
		return msg_no;
	}

	@JsonIgnore
	public void setMsgNo(String msg_no) {
		this.msg_no = msg_no;
	}

	@JsonIgnore
	public String getMsgTime() {
		return msg_time;
	}

	@JsonIgnore
	public void setMsgTime(String msg_time) {
		this.msg_time = msg_time;
	}

	@JsonIgnore
	public String getSignType() {
		return sign_type;
	}

	@JsonIgnore
	public void setSignType(String sign_type) {
		this.sign_type = sign_type;
	}

	@JsonIgnore
	public String getVer() {
		return ver;
	}

	@JsonIgnore
	public void setVer(String ver) {
		this.ver = ver;
	}

	@JsonIgnore
	public String getRetcode() {
		return retcode;
	}

	@JsonIgnore
	public void setRetcode(String retcode) {
		this.retcode = retcode;
	}

	@JsonIgnore
	public String getErrmsg() {
		return errmsg;
	}

	@JsonIgnore
	public void setErrmsg(String errmsg) {
		this.errmsg = errmsg;
	}
}
